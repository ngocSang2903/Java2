    private void butthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butthemActionPerformed
        // TODO add your handling code here:
        if (txtten.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên");
        }
        if (txtgiave.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập giá vé");
        } else if (!txtgiave.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Giá vé phải là số và lớn hơn 0");
        }

        ChieuP cp = new ChieuP();
        cp.setTen(txtten.getText());
        if (rahd.isSelected()) {
            cp.setTheloai("Hanh dong");
        } else if (ratc.isSelected()) {
            cp.setTheloai("Tinh cam");
        } else if (rakd.isSelected()) {
            cp.setTheloai("Kinh di");
        } else if (rahh.isSelected()) {
            cp.setTheloai("Hai huoc");
        }
        cp.setGiave(Integer.parseInt(txtgiave.getText()));
        cp.setHangphim((String) cbtheloai.getSelectedItem());
        list.add(cp);
        addtable();


    }//GEN-LAST:event_butthemActionPerformed

    private void butxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butxoaActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        list.remove(row);
        addtable();
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_butxoaActionPerformed

    private void butghiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butghiActionPerformed
        // TODO add your handling code here:
        try {
            FileOutputStream fos = new FileOutputStream("a.dat");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(list);
            oos.close();
            JOptionPane.showMessageDialog(this, "Ghi thành công");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_butghiActionPerformed

    private void butmoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butmoActionPerformed
        // TODO add your handling code here:
        try {
            FileInputStream fis = new FileInputStream("a.dat");
            ObjectInputStream ois = new ObjectInputStream(fis);
            list = (ArrayList<ChieuP>) ois.readObject();
            if (list.size() >= 0) {
                addtable();
                JOptionPane.showMessageDialog(this, "Đọc file thành công");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_butmoActionPerformed

