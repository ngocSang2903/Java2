    public void addtable() {
        DefaultTableModel bang = (DefaultTableModel) table.getModel();
        bang.setRowCount(0);
        for (ChieuP cp : list) {
            bang.addRow(new Object[]{cp.getTen(), cp.getTheloai(), cp.getGiave(), cp.getHangphim()});
        }
    }
    int index;

    public void display() {
        ChieuP cp = list.get(index);
        txtten.setText(cp.getTen());
        if (cp.getTheloai().equals("Hanh dong")) {
            rahd.setSelected(true);
        } else if (cp.getTheloai().equals("Tinh cam")) {
            ratc.setSelected(true);
        } else if (cp.getTheloai().equals("Kinh di")) {
            rakd.setSelected(true);
        } else if (cp.getTheloai().equals("Hai huoc")) {
            rahh.setSelected(true);
        }
        txtgiave.setText(cp.getGiave() + "");
        cbtheloai.setSelectedItem(cp.getHangphim());
    }

