    @Override
    public void run() {
        String chuoi=text.getText();
        while (true) {            
            String ch=chuoi.substring(0,1);
            chuoi=chuoi.substring(1).concat(ch);
            text.setText(chuoi);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(ChieuPhim.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    

