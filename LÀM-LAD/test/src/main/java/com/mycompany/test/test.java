    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtten.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
        }
        if(txtgia.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Giá không được để trống");
        }else if(!txtgia.getText().matches("\\d+")){
              JOptionPane.showMessageDialog(this, "Giá phải là số và lớn hơn không");
        }else{
        Sach s=new Sach();
        s.setTen(txtten.getText());
        s.setTheloai((String) cbtheloai.getSelectedItem());
        s.setGia(Integer.parseInt(txtgia.getText()));
        list.add(s);
        addtable();
        ghi();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

