    public void addtable(){
        DefaultTableModel bang=(DefaultTableModel)table.getModel();
        bang.setRowCount(0);
        for (Sach s : list) {
            bang.addRow(new Object[]{s.getTen(),s.getTheloai(),s.getGia(),s.getStatus()});
        }
    }
    public void ghi(){
        try {
            FileOutputStream fos =new FileOutputStream("a.dat");
            ObjectOutputStream oos=new ObjectOutputStream(fos);
            oos.writeObject(list);
            oos.close();
            JOptionPane.showMessageDialog(this, "ghi thành công");
        } catch (Exception e) {
        }
    }
