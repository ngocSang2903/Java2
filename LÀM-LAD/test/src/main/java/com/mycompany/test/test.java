    @Override
    public void run() {
        int gio=0;
        int phut=0;
        int giay=0;
        while (true) {            
            giay++;
            if(giay==60){
                phut++;
                giay=0;
            }
            if(phut==60){
                gio++;
                phut=0;
            }
            if(gio==24){
                phut=0;
                giay=0;
                gio=0;
            }
            
            text.setText(gio+" : "+phut+"  : "+giay);
            try {
                Thread.sleep(10);
            } catch (InterruptedException ex) {
                Logger.getLogger(test.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }


