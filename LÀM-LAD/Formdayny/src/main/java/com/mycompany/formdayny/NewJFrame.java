    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        txtma.setText(table.getValueAt(row, 0).toString());
        txtten.setText(table.getValueAt(row, 1).toString());
        txttuoi.setText(table.getValueAt(row, 2).toString());
        if (table.getValueAt(row, 3).toString().equals("Nam")) {
            txtnam.setSelected(true);
        } else {
            txtnu.setSelected(true);
        }
        txtdiachi.setSelectedItem(table.getValueAt(row, 4));
        txttrangthai.setSelected(table.getValueAt(row, 5).equals(true));
    }//GEN-LAST:event_tableMouseClicked

    private void buttimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttimActionPerformed
        // TODO add your handling code here:
        for (Khachhang kh : list) {
            ArrayList<Khachhang> sc=new ArrayList<>();
            if (kh.getMakh().equalsIgnoreCase(txttim.getText())) {
                
                
                int ind = list.indexOf(kh);
                txtma.setText(kh.getMakh());
                txtten.setText(kh.getTenkh());
                txttuoi.setText(kh.getTuoi() + "");
                if (kh.getGioitinh().equals("Nam")) {
                    txtnu.setSelected(true);
                } else {
                    txtnam.setSelected(true);
                }
                txtdiachi.setSelectedItem(kh.getDiachi());
                txttrangthai.setSelected(kh.isTrangthai());
                sc.add(kh);
                addtable(sc);
                table.setRowSelectionInterval(ind, ind);
            }
        }
    }//GEN-LAST:event_buttimActionPerformed

