    public void addtable(ArrayList<Khachhang> list) {
        DefaultTableModel bang = (DefaultTableModel) table.getModel();
        bang.setRowCount(0);
        for (Khachhang kh : list) {
            bang.addRow(new Object[]{kh.getMakh(), kh.getTenkh(), kh.getTuoi(), kh.getGioitinh(), kh.getDiachi(), kh.isTrangthai()});
        }
    }
    int index;

    public void display() {
        Khachhang kh = list.get(index);
        int vip = list.indexOf(kh);
        txtma.setText(kh.getMakh());
        txtten.setText(kh.getTenkh());
        txttuoi.setText(kh.getTuoi() + "");
        if (kh.getGioitinh().equals("Nam")) {
            txtnu.setSelected(true);
        } else {
            txtnam.setSelected(true);
        }
        txtdiachi.setSelectedItem(kh.getDiachi());
        txttrangthai.setSelected(kh.isTrangthai());
        txtso.setText("Record " + (index + 1) + " of " + list.size());
        table.setRowSelectionInterval(vip, vip);
    }

