    private void butthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butthemActionPerformed
        // TODO add your handling code here:
        if (vui == -1) {
            Khachhang kh = new Khachhang();
            kh.setMakh(txtma.getText());
            kh.setTenkh(txtten.getText());
            kh.setTuoi(Integer.parseInt(txttuoi.getText()));
            if (txtnam.isSelected()) {
                kh.setGioitinh("Nam");
            } else {
                kh.setGioitinh("Nữ");
            }
            kh.setDiachi((String) txtdiachi.getSelectedItem());
            kh.setTrangthai(txttrangthai.isSelected());
            list.add(kh);
            addtable(list);
        } else {
            int sua = table.getSelectedRow();
            Khachhang kh = list.get(sua);
            kh.setMakh(txtma.getText());
            kh.setTenkh(txtten.getText());
            kh.setTuoi(Integer.parseInt(txttuoi.getText()));
            if (txtnam.isSelected()) {
                kh.setGioitinh("Nam");
            } else {
                kh.setGioitinh("Nữ");
            }
            kh.setDiachi((String) txtdiachi.getSelectedItem());
            kh.setTrangthai(txttrangthai.isSelected());
            addtable(list);
        }
    }//GEN-LAST:event_butthemActionPerformed

